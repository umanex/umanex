/** Fallback color tokens for values not yet in Tokens Studio. */
const COLOR_FALLBACKS = {
  text: { inverse: '{color.neutral.950}' },
  border: {
    'border-default': '{color.neutral.700}',
    'border-subtle': '{color.neutral.800}',
    'border-focus': '{color.blue.300}',
  },
};

// ---------------------------------------------------------------------------
// Format: ts/colors
// ---------------------------------------------------------------------------

function formatColors() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  const primSet = raw[setOrder[0]] || {};
  const tokSet = raw[setOrder[1]] || {};

  function colorScale(colorName) {
    const group = primSet.color?.[colorName] || {};
    return Object.keys(group)
      .filter(k => !k.startsWith('$'))
      .map(k => [k, lit(flat[`color.${colorName}.${k}`])]);
  }

  function semanticColorGroup(groupPath, stripPrefix = '') {
    const parts = groupPath.split('.');
    let group = tokSet;
    for (const p of parts) group = group?.[p] || {};
    return Object.keys(group)
      .filter(k => !k.startsWith('$') && typeof group[k] === 'object' && 'value' in group[k])
      .map(k => {
        const cleanKey = stripPrefix && k.startsWith(`${stripPrefix}-`)
          ? k.slice(stripPrefix.length + 1)
          : k;
        return [kebabToCamel(cleanKey), lit(flat[`${groupPath}.${k}`])];
      });
  }

  const neutralEntries = colorScale('neutral');
  const blueEntries = colorScale('blue');
  const bgEntries = semanticColorGroup('color.background', 'bg');
  const brandEntries = semanticColorGroup('color.brand', 'brand');
  const textEntries = semanticColorGroup('color.text', 'text');
  const statusEntries = semanticColorGroup('color.status');
  let borderEntries = semanticColorGroup('color.border', 'border');

  // Apply fallbacks for missing tokens
  for (const [k, ref] of Object.entries(COLOR_FALLBACKS.text)) {
    if (!textEntries.find(([ek]) => ek === k)) {
      textEntries.push([k, lit(resolve(ref, flat))]);
    }
  }
  if (borderEntries.length === 0) {
    borderEntries = Object.entries(COLOR_FALLBACKS.border).map(([k, ref]) => {
      const cleanKey = k.startsWith('border-') ? k.slice(7) : k;
      return [kebabToCamel(cleanKey), lit(resolve(ref, flat))];
    });
  }

  // --- Component tokens (from components set) ---
  const compSet = raw[setOrder[2]] || {};
  const buttonGroup = compSet.button || {};
  const buttonVariants = [];

  for (const [variant, def] of Object.entries(buttonGroup)) {
    if (variant.startsWith('$')) continue;
    if (typeof def === 'object' && 'value' in def) {
      // top-level prop like radius — skip for buttonTokens
      continue;
    }
    const props = [];
    for (const [prop, val] of Object.entries(def)) {
      if (prop.startsWith('$') || typeof val !== 'object' || !('value' in val)) continue;
      props.push([prop, lit(resolve(String(val.value), flat))]);
    }
    if (props.length > 0) {
      const inner = props.map(([k, v]) => `      ${k}: ${v},`).join('\n');
      buttonVariants.push(`    ${variant}: {\n${inner}\n    }`);
    }
  }

  const buttonRadius = buttonGroup.radius?.value
    ? `    radius: ${lit(resolve(String(buttonGroup.radius.value), flat))},`
    : '';

  const buttonTokensBody = buttonVariants.length > 0
    ? `{\n${buttonVariants.join(',\n')},\n${buttonRadius ? `${buttonRadius}\n` : ''}  }`
    : '{}';

  return `${HEADER}
export const neutral = ${obj(neutralEntries)} as const;

export const blue = ${obj(blueEntries)} as const;

export const background = ${obj(bgEntries)} as const;

export const brand = ${obj(brandEntries)} as const;

export const text = ${obj(textEntries)} as const;

export const status = ${obj(statusEntries)} as const;

export const border = ${obj(borderEntries)} as const;

export const buttonTokens = ${buttonTokensBody} as const;

export const colors = {
  neutral,
  blue,
  background,
  brand,
  text,
  status,
  border,
  buttonTokens,
} as const;
`;
}

