function formatSpacing() {
  const { raw, flat, setOrder } = loadResolvedTokens();
  const tokSet = raw[setOrder[1]] || {};
  const spacingGroup = tokSet.spacing || {};

  const spaceEntries = [];
  const layoutEntries = [];

  for (const [k, v] of Object.entries(spacingGroup)) {
    if (k.startsWith('$') || typeof v !== 'object' || !('value' in v)) continue;
    const resolved = flat[`spacing.${k}`];

    if (k.startsWith('space-')) {
      const cleanKey = k.slice(6);
      spaceEntries.push([cleanKey, lit(resolved)]);
    } else {
      layoutEntries.push([kebabToCamel(k), lit(resolved)]);
    }
  }

  return `${HEADER}
export const space = ${obj(spaceEntries)} as const;

export const layout = ${obj(layoutEntries)} as const;

export const spacing = {
  space,
  layout,
} as const;
`;
}

